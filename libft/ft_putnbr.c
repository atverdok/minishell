#include "libft.h"

void	ft_putnbr(int n)
{
	long int res;

	res = n;
	if (res < 0)
	{
		res *= (-1);
		ft_putchar('-');
	}
	if (res > 9)
	{
		ft_putnbr(res / 10);
	}
	ft_putchar(res % 10 + '0');
}
