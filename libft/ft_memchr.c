#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*ptr;
	size_t	i;

	ptr = (char *)s;
	i = 0;
	while (i < n)
	{
		if (*ptr == (char)c)
			return ((void *)ptr);
		i++;
		ptr++;
	}
	return (NULL);
}
